void pre_auton(void) {
  // Initializing Robot Configuration. DO NOT REMOVE!
  vexcodeInit();
  
  // Calibrate the Inertial Sensor
  Angle.calibrate();
  while (Angle.isCalibrating()) {
    wait(1, msec);
  }

  // Exrta Stuff
  MCL::StartMCL(0, -60, 0);
  
  /* Starting Threads */
  thread(MCL::MonteCarlo).detach();
  while (true) {
    
    wait(5, msec);
  }
}

